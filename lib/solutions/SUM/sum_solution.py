def compute(x: int, y: int) -> int:
    """
    Compute the sum of two positive integers.

    Args:
        x (int): A positive integer between 0 and 100.
        y (int): A positive integer between 0 and 100.

    Returns:
        int: The sum of the two integers.
    """
    return x + y



